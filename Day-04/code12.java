// Write a java program example for type casting of short , long, int.
public class code12 {

    public static void main(String[]args)
    {
        short a = (short) 789456123;
        long b = 1234567890;
        int c = (int)b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);


    }
    
}
