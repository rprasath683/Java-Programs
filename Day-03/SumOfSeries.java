//   The program to calculate the sum of a different series, such as 11 + 13 + 15 + ⋯ + 11911 + 31 +51 + ⋯ + 191 ?
public class SumOfSeries
{
    public static void main(String args[])
    {
        double n=0;
        for(int i=1;i<10;i++)
        {
    n=n+(1.0/i);    //   1+1\1===2---2+1\2==2


        }
        System.out.println(n);
       
    }
}
