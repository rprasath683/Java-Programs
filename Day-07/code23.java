// Java program to display even and odd numbers from 1 to 100.
public class code23 {
    public static void main(String args[]) {
        for (int i = 1; i <= 100; i++) {
            if (i % 2 == 0) {
                System.out.println("even " + i);
            } else {
                System.out.println("odd " + i);
            }
        }
    }
}
